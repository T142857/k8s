K8S_MASTER_01_NAME=labs-k8s-master-01
K8S_MASTER_02_NAME=labs-k8s-master-02
K8S_MASTER_03_NAME=labs-k8s-master-03

echo << EOF > /etc/etcd.env
ETCD_DATA_DIR=/var/lib/etcd
ETCD_ADVERTISE_CLIENT_URLS=https://${K8S_MASTER_01}:2379
ETCD_INITIAL_ADVERTISE_PEER_URLS=https://${K8S_MASTER_01}:2380
ETCD_INITIAL_CLUSTER_STATE=new
ETCD_LISTEN_CLIENT_URLS=https://${K8S_MASTER_01}:2379
ETCD_ELECTION_TIMEOUT=5000
ETCD_HEARTBEAT_INTERVAL=250
ETCD_LISTEN_PEER_URLS=https://${K8S_MASTER_01}:2380
ETCD_NAME=${K8S_MASTER_01_NAME}
ETCD_PROXY=off
ETCD_INITIAL_CLUSTER=${K8S_MASTER_01_NAME}=https://${K8S_MASTER_01}:2380,${K8S_MASTER_02_NAME}=https://${K8S_MASTER_02}:2380,${K8S_MASTER_03_NAME}=https://${K8S_MASTER_03}:2380

# TLS settings
ETCD_TRUSTED_CA_FILE=/etc/ssl/etcd/ssl/ca.pem
ETCD_CERT_FILE=/etc/ssl/etcd/ssl/member-${K8S_MASTER_01}.pem
ETCD_KEY_FILE=/etc/ssl/etcd/ssl/member-${K8S_MASTER_01}-key.pem
ETCD_PEER_TRUSTED_CA_FILE=/etc/ssl/etcd/ssl/ca.pem
ETCD_PEER_CERT_FILE=/etc/ssl/etcd/ssl/member-${K8S_MASTER_01}-01.pem
ETCD_PEER_KEY_FILE=/etc/ssl/etcd/ssl/member-${K8S_MASTER_01}-key.pem
